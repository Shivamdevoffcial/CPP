#include <bits/stdc++.h>
using namespace std;

int main() {
	// program to print the memory sizes of data types
    int num;
    char name;
    bool flag;
    double number;
    float digit;
    cout<<"size of num is "<<sizeof(num)<<endl;
    cout<<"size of num is "<<sizeof(name)<<endl;
    cout<<"size of flag is "<<sizeof(flag)<<endl;
    cout<<"size of number is "<<sizeof(number)<<endl;
    cout<<"size of digit is "<<sizeof(digit)<<endl;
	return 0;
}
